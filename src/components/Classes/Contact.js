class Contact {
    constructor(assignedCustomer, email, phoneNumber, name, involvedProjects, notes, personality) {
        this.assignedCustomer = assignedCustomer;
        this.email = email;
        this.phoneNumber = phoneNumber;
        this.name = name;
        this.involvedProjects = involvedProjects;
        this.notes = notes;
        this.personality = personality;
    }
}