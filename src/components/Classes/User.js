class User {
  constructor(name, email, password, seniority, skills, assignedTasks, rate, personality) {
    this.name = name;
    this.email = email;
    this.password = password;
    this.seniority = seniority;
    this.skills = skills;
    this.assignedTasks = assignedTasks;
    this.rate = rate;
    this.personality = personality;
  }
}